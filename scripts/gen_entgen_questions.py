def esc(txt):
    """Escape single quotes for SQL."""
    return txt.replace("'", "''")

def make_sql(questions):
    """Build an INSERT statement for a list of (q, opts, ans, expl, diff, ch) tuples."""
    lines = []
    lines.append('INSERT INTO "Question" (id, "tradeId", "chapterId", type, difficulty, question, options, answer, explanation, locale, "createdAt", "updatedAt") VALUES')
    vals = []
    for i, q in enumerate(questions):
        num = q[0]  # question number 1-480
        qtext = esc(q[1])
        opts_json = json.dumps(q[2], ensure_ascii=False)
        answer = q[3]
        explanation = esc(q[4])
        difficulty = q[5]
        chapter = q[6]
        vals.append(f"('q_entgen_{num:03d}', '{TRADE}', '{chapter}', 'MCQ', '{difficulty}', '{qtext}', '{opts_json}', '{answer}', '{explanation}', 'fr', '{TS}', '{TS}')")
    lines.append(",\n".join(vals))
    lines.append("ON CONFLICT (id) DO NOTHING;\n")
    return "\n".join(lines)

